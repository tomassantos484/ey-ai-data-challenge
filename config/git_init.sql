  GIT_CREDENTIALS = EYADC.INTEGRATIONS.GH_PAT
  ORIGIN = 'https://github.com/tomassantos484/ey-ai-data-challenge.git';

ALTER GIT REPOSITORY EYADC.REPO.EY_AI_DATA_CHALLENGE FETCH;
SHOW API INTEGRATIONS LIKE 'EY_GITHUB_API_INT';
